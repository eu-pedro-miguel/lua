-- Podem números inteiros negativos e número fracionários(float/double)

-- NÚMEROS NEGATIVOS

-- 1° Etapa

do
	a = 10
	b = 5

	c = a - b

	print('1° Etapa: ' .. a .. ' - ' .. b .. ' = ' .. c)
end

-- 2° Etapa

do
	d = - 5
	e = 10

	f = d - e

	print('2° Etapa: ' .. d .. ' - ' .. e .. ' = ' .. f)
end

-- 3° Etapa

do
	g = 5
	h = 10

	i = g - h

	print('3° Etapa: ' .. g .. ' - ' .. h .. ' = ' .. i)
end

-- 4° Etapa

do
	j = - 5
	k = - 10

	l = j - k

	print('4° Etapa: ' .. j .. ' - ' .. k .. ' = ' .. l)
end


print(string.rep('-', 30)) -- rep = Repetir

-- NÚMEROS FLOAT/DOUBLE

-- 5° Etapa

do
	a = 10.5
	b = 2

	c = a * b

	print('5° Etapa: ' .. a .. ' x ' .. b .. ' = ' .. c)
end

-- 6° Etapa

do
	d = - 5
	e = 10.5

	f = d * e

	print('6° Etapa: ' .. d .. ' x ' .. e .. ' = ' .. f)
end

-- 7° Etapa

do
	g = 5.5
	h = 5.5

	i = g * h

	print('7° Etapa: ' .. g .. ' x ' .. h .. ' = ' .. i)
end

-- 8° Etapa

do
	j = - 5.2666
	k = - 2.5

	l = j * k

	print('8° Etapa: ' .. j .. ' x ' .. k .. ' = ' .. l)
end




